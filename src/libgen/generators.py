class ConstantsCodeGenerator(FileGenerator):
    module_name = "dearpypixl_lib_constants.py"

    def __init__(self, metadata: DearPyGuiMetadata, /) -> None:
        super().__init__(metadata)
        self._exports = []

    class _NodeTransformer(ast.NodeTransformer):
        def __init__(self, *args, **kwargs) -> None:
            super().__init__(*args, **kwargs)
            self._const_refs = []

        def visit_Import(self, node: ast.Import):
            return

        def visit_ImportFrom(self, node: ast.ImportFrom):
            return

        def visit_Assign(self, node: ast.Assign):
            self.generic_visit(node)

            if not isinstance(node.value, ast.Attribute):
                return node

            attr_node = node.value
            if not isinstance(attr_node.value, ast.Name):
                return node

            name = attr_node.value.id
            attr = attr_node.attr

            if name.endswith("dearpygui") and attr.startswith("mv"):
                self._const_refs.append(attr)

            return node

    @staticmethod
    def _get_const_namespace_map(constants: typing.Sequence[str], /):
        capwords = re.compile(r"[A-Z][a-z0-9]*")

        d = collections.defaultdict(list); d['']

        for const in sorted(constants):
            key, _, suffix = const.partition("_")
            if not suffix:
                d[''].append(('', const))
                continue
            parts = suffix.split("_")

            i = 0
            for i in range(len(parts)):
                part = parts[i]

                words = capwords.findall(part)
                if not words:
                    words = (part,)

                parts.extend(words)

            del parts[:i+1]

            name = '_'.join(parts).upper()

            d[key].append((name, const))

        for ns2, ns1 in (("mvNodesCol", "mvNodeCol"), ("mvNodesStyleVar", "mvNodeStyleVar")):
            node_theme_consts2 = d.pop(ns2, None)
            if node_theme_consts2 is not None:
                node_theme_consts1 = d.get(ns1)
                if node_theme_consts1 is not None:
                    node_theme_consts1.extend(node_theme_consts2)
                    node_theme_consts1.sort()
                else:
                    d[ns1] = node_theme_consts2

        return d

    def _get_const_module_info(self, /):
        nodes = ast.parse(inspect.getsource(self.module))

        transformer = self._NodeTransformer()
        transformer.visit(nodes)

        return ast.unparse(nodes), transformer._const_refs

    def _generate_enum(self, enum_name, constants, /):
        buffer = []

        buffer.append(f"class {enum_name}(enum.IntEnum):")
        for (member, const) in constants:
            buffer.append(f"    {member or const} = _dearpygui.{const}")
        buffer.append('')

        for (member, const) in constants:
            buffer.append(f"{const} = {enum_name}.{member or const}")
            self._exports.append(const)
        buffer.append('')

        buffer.append('')
        return buffer

    def generate(self):
        content = []

        content.extend(self._generate_banner("DearPyPixl Constants Library"))

        content.append('import enum')
        content.append('')
        content.append('from dearpygui import _dearpygui')
        content.append('')
        content.append('')

        namespace_map = self._get_const_namespace_map(self.metadata.constants)

        buffer = []

        buffer.extend(self._generate_enum("DearPyGui", namespace_map.pop('', ())))

        source, const_refs = self._get_const_module_info()

        for enum_name, member_const_pairs in namespace_map.items():
            member_const_pairs = [p for p in member_const_pairs if p[1] not in const_refs]
            if member_const_pairs:
                buffer.extend(self._generate_enum(enum_name, member_const_pairs))

        # apply some basic formatting to the "unparse"d source
        indentation = 0
        for ln in source.splitlines():
            if ln.startswith("class "):
                buffer.append("")
                indentation = max(0, indentation - 4)
            elif ln:
                n_spaces = len(ln) - len(ln.lstrip())
                if not n_spaces:
                    if indentation: buffer.append('')
                    indentation = 0
                elif n_spaces > indentation:
                    indentation =+ n_spaces
                elif indentation > n_spaces:
                    indentation -= n_spaces
                    buffer.append('')
            buffer.append(ln)

        content.append('__all__ = (')
        for name in self._exports:
            content.append(f'    "{name}",')
        content.append(')')
        content.append('')
        content.append('')
        content.append('')
        content.append('')

        content.extend(buffer)

        apply_newlines(content)
        return content


