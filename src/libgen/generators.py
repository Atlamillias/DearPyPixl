class _ItemsGenerator(FileGenerator):
    __slots__ = ("metadata",)

    mimetype = "text/x-python"
    module_name = "dearpypixl_lib_items.py"

    _FFLAG_TO_STATES = {
        FeatureFlag.SUPPORTS_HOVER         : ("hovered",),
        FeatureFlag.SUPPORTS_ACTIVE        : ("active",),
        FeatureFlag.SUPPORTS_FOCUS         : ("focused",),
        FeatureFlag.SUPPORTS_CLICK         : ("clicked", "left_clicked", "right_clicked", "middle_clicked",),
        FeatureFlag.SUPPORTS_VISIBILITY    : ("visible",),
        FeatureFlag.SUPPORTS_EDITING       : ("edited",),
        FeatureFlag.SUPPORTS_ACTIVATION    : ("activated", "deactivated"),
        FeatureFlag.SUPPORTS_DEACTIVATIONAE: ("deactivated_after_edit",),
        FeatureFlag.SUPPORTS_TOGGLE        : ("toggled_open",),
        FeatureFlag.SUPPORTS_GEOMETRY      : ("rect_min", "rect_max"),
        FeatureFlag.SUPPORTS_RECTSIZE      : ("rect_size", "resized"),
        FeatureFlag.SUPPORTS_POSITIONING   : ("pos",),
        FeatureFlag.SUPPORTS_CONTREGION    : ("content_region_avail",),
        FeatureFlag.SUPPORTS_SCROLLING     : ("scrolled", "is_scrolling", "scroll_pos", "scroll_max",),
    }
    _STATE_TYPE_MAP = collections.defaultdict(lambda: "bool", {
        "rect_min"              : "Array[int, Literal[2]]",
        "rect_max"              : "Array[int, Literal[2]]",
        "rect_size"             : "Array[int, Literal[2]]",
        "pos"                   : "Array[int, Literal[2]]",
        "content_region_avail"  : "Array[int, Literal[2]]",
        "scroll_pos"            : "Array[float, Literal[2]]",
        "scroll_max"            : "Array[float, Literal[2]]",
    })
    _SKIPPED_ASSIGNMENTS = ("label", "use_internal_label", "user_data")

    def __init__(self, metadata: DearPyGuiMetadata, /) -> None:
        super().__init__(metadata)
        self._shared_properties = dict()

    @staticmethod
    def _get_configuration_null_value(parameter: metadata.Parameter, /) -> str | None:
        if parameter.kind != parameter.KEYWORD_ONLY or parameter.flags & ParameterFlag.NO_WRITE:
            return

        anno = parameter.annotation
        if anno is parameter.empty:
            return

        default = parameter.default
        if default is parameter.empty:
            return

        if default == "None" and anno.endswith("None"):
            return "None"

        if default == '0' and (anno == "Item" or anno == "int | str" or anno == "str | int"):
            return '0'

        if (default == "''" or default == '""') and anno == "str":
            return "''"

    @staticmethod
    def _is_common_parent(type_info: ItemTypeInfo, /) -> bool:
        return type_info.flags & FeatureFlag.PARENT > 0 and not type_info.children

    @staticmethod
    def _is_common_child(type_info: ItemTypeInfo, /) -> bool:
        return type_info.flags & FeatureFlag.CHILD > 0 and not type_info.parents

    @staticmethod
    def _get_command_names(type_info: ItemTypeInfo, /) -> tuple[str, ...]:
        command = type_info.command

        if type_info.flags & FeatureFlag.PARENT:
            if command.startswith("draw_"):
                return (command.removeprefix("draw_"), command)

            return (command.removeprefix("add_"), command)

        return (command,)

    @staticmethod
    def _iter_positional_parameters(type_info: ItemTypeInfo, /):
        for p in type_info.parameters:
            if p.kind == p.POSITIONAL_ONLY or p.kind == p.POSITIONAL_OR_KEYWORD:
                yield p

    @staticmethod
    def _iter_keyword_parameters(type_info: ItemTypeInfo, /):
        for p in type_info.parameters:
            if p.kind == p.KEYWORD_ONLY:
                yield p

    @staticmethod
    def _iter_readonly_config(type_info: ItemTypeInfo, /):
        for p in type_info.parameters:
            if p.flags & ParameterFlag.NO_WRITE and not (p.flags & ParameterFlag.NO_READ):
                yield p

    @staticmethod
    def _iter_readable_config(type_info: ItemTypeInfo, /):
        for p in type_info.parameters:
            if not (p.flags & ParameterFlag.NO_READ):
                yield p

    @staticmethod
    def _iter_writable_config(type_info: ItemTypeInfo, /):
        for p in type_info.parameters:
            if not (p.flags & ParameterFlag.NO_WRITE):
                yield p

    @staticmethod
    def _iter_states(type_info: ItemTypeInfo, /):
        flags = type_info.flags
        for flag in FeatureFlag:
            states = __class__._FFLAG_TO_STATES.get(flag)
            if states and flags & flag:
                yield from states


class ItemsCodeGenerator(_ItemsGenerator):

    class _NodeTransformer(ast.NodeTransformer):
        def visit_arg(self, node: ast.arg):
            node.annotation = None
            return node

        def visit_ClassDef(self, node: ast.ClassDef) -> typing.Any:
            if len(node.body) == 1:
                n = node.body[0]
                node_type = type(n)
                if (
                    node_type is ast.Pass or
                    (
                        node_type is ast.Expr and
                        isinstance(n.value, ast.Constant) and  # type: ignore
                        n.value.value is ...  # type: ignore
                    )
                ):
                    node.body.clear()

                    return node

            return self.generic_visit(node)

        def visit_FunctionDef(self, node: ast.FunctionDef) -> typing.Any:
            ast.fix_missing_locations(self.generic_visit(node))

            if (
                len(node.body) > 0 and isinstance((n := node.body[0]), ast.Expr) and
                isinstance(n.value, ast.Constant) and isinstance(n.value.value, str)
            ):
                node.body.pop(0)

            node.returns = None
            node.lineno += 1

            return node

    def __init__(self, metadata: DearPyGuiMetadata, /) -> None:
        super().__init__(metadata)
        self._late_assignments: dict[str, dict[str, str | list[str]]] = collections.defaultdict(dict)

    def _generate_property(self, name, getter, setter = None, default = MISSING, /, *, level: int = 0, no_rename: bool = False):
        if not isinstance(getter, str):
            getter = getter.__name__

        identifier = name if no_rename else f"_property__{name}"

        buffer = [
            f"@property",
            f"def {identifier}(self, /, *, __func=_dearpygui.{getter}):",
            f"    return __func(self)[\"{name}\"]",
        ]

        if setter is not None:
            if not isinstance(setter, str):
                setter = setter.__name__

            buffer.extend((
                f"@{identifier}.setter",
                f"def {identifier}(self, value, /, *, __func=_dearpygui.{setter}):",
                f"    __func(self, {name}=value)",
            ))

            if default is not MISSING:
                buffer.extend((
                    f"@{identifier}.deleter",
                    f"def {identifier}(self, /, *, __func=_dearpygui.{setter}):",
                    f"    __func(self, {name}={default})",
                ))

        buffer.append('')

        apply_indents(buffer, level)
        return buffer

    def _generate_class(self, type_info: ItemTypeInfo, /, *, level: int = 0):
        buffer = []

        type_name = type_info.name


        # class declaration

        flags = type_info.flags

        bases = []
        if flags & FeatureFlag.ADDRESSABLE:
            bases.append("SupportsValueArray")
        if flags & FeatureFlag.PARENT:
            bases.append("ContainerItem")
        if flags & FeatureFlag.CHILD:
            bases.append("ChildItem")
        assert len(bases) > 0

        buffer.append(f"class {type_name}({', '.join(bases)}, command=\"{type_info.command}\", slot={type_info.target_slot}):")
        del bases, buffer[:-1]


        members_seen = set()


        # attributes

        buffer.append("    __slots__ = ()")
        buffer.append('')
        members_seen.add("__slots__")


        # constructor

        parameters = []
        arguments  = []
        for p in type_info.parameters:
            parameters.append(p.replace(annotation=p.empty))

            if p.kind == p.POSITIONAL_ONLY or p.kind == p.POSITIONAL_OR_KEYWORD:
                arguments.append(p.name)
            elif p.kind == p.KEYWORD_ONLY:
                arguments.append(f"{p.name}={p.name}")
            else:
                raise AssertionError("parameter is variadic")

        signature = repr(inspect.Signature(parameters)) \
            .partition('(')[-1] \
            .rpartition(')')[0]

        buffer.append(f"    @classmethod")
        buffer.append(f"    def create(cls, /, {signature}, __func=_dearpygui.{type_info.command}, **kwargs):  # ty: ignore[invalid-method-override]")
        buffer.append(f"        return cls(tag=__func({", ".join(arguments)}))  # ty: ignore[invalid-argument-type]")
        # HACK: make the `item_type` argument of `mvThemeComponent.create()` compatible
        # with item type classes
        if type_name == "mvThemeComponent":
            buffer[-1] = buffer[-1].replace("item_type", "int(item_type)", count=1)

        buffer.append("")
        members_seen.add("create")
        del parameters, arguments, signature


        # read-only configuration

        for p in self._iter_readonly_config(type_info):
            buffer.extend(self._generate_property(p.name, "get_item_configuration", level=1, no_rename=True))


        # read/write configuration

        with apply_linebreak(buffer):
            for p in self._iter_writable_config(type_info):
                if p.name in members_seen:
                    continue

                if p.name not in self._SKIPPED_ASSIGNMENTS:
                    buffer.append(f"    {p.name} = _property__{p.name}")
                    self._shared_properties[p.name] = p

                members_seen.add(p.name)


        # states

        with apply_linebreak(buffer):
            for state in self._iter_states(type_info):
                if state not in members_seen:
                    buffer.append(f"    {state} = _property__{state}")
                    members_seen.add(state)

            if type_info.flags & FeatureFlag.SUPPORTS_RECTSIZE and not (type_info.flags & FeatureFlag.SUPPORTS_GEOMETRY):
                for state in ("rect_min", "rect_max"):
                    assert state not in members_seen
                    buffer.append(f"    {state} = _property__{state}2")

            if type_info.flags & FeatureFlag.SUPPORTS_SCROLLING:
                for s in ("x", "y"):
                    buffer.append(f"    {s}_scroll_max = _property__{s}_scroll_max")
                    buffer.append(f"    {s}_scroll_pos = _property__{s}_scroll_pos")


        # other members

        proto_type = getattr(self.module, type_name, None)
        if proto_type is not None:
            source = inspect.getsource(proto_type)

            module_node = ast.parse(source); assert len(module_node.body) == 1
            class_node  = self._NodeTransformer().visit(module_node.body.pop(0))

            for node in class_node.body:
                s = ast.unparse(node)

                if isinstance(node, ast.Assign):
                    child_node = node.value
                    name_node  = getattr(child_node, "value", None)
                    if isinstance(child_node, ast.Attribute) and isinstance(name_node, ast.Name) and name_node.id.startswith("mv"):
                        self._late_assignments[type_name][child_node.attr] = s.partition("=")[-1].strip()
                        continue
                    elif isinstance(child_node, ast.Name) and child_node.id.startswith("mv"):
                        for name_node in node.targets:
                            self._late_assignments[type_name][name_node.id] = s.partition("=")[-1].strip()
                        continue

                elif isinstance(node, ast.FunctionDef):
                    if any(getattr(n, "id", None) == "overload" for n in node.decorator_list):
                        continue
                    node.type_params.clear()
                    s = ast.unparse(node)

                for ln in s.splitlines():
                    buffer.append(f"    {ln}")
                buffer.append('')


        apply_indents(buffer, level)
        return buffer

    def generate(self, /) -> list:
        content = []

        content.extend(self._generate_banner("DearPyPixl Type Definitions Library"))


        content.append("from dearpypixl.core.appitem import *")
        content.append("")
        content.append("from dearpygui import dearpygui, _dearpygui")
        content.append("")
        content.append("")
        content.append("")
        content.append("")


        # populates `self._symbols`
        buffer = []
        for type_info in self.metadata.typedef:
            buffer.extend(self._generate_class(type_info))

            type_name = type_info.name

            commands = self._get_command_names(type_info)
            if len(commands) > 1:
                command, command2 = commands
                buffer.append(f"{command} = {command2} = {type_name}.create")
            else:
                command = commands[0]
                buffer.append(f"{command} = {type_name}.create")
            buffer.append('')
            buffer.append('')

        content.append("# [ shared objects ]")
        content.append('')

        states = set()
        content.append('# state properties')
        content.append('')
        for _states in self._FFLAG_TO_STATES.values():
            states.update(_states)
        for state in sorted(states):
            if state == "pos":
                content.extend(self._generate_property(state, "get_item_state", "configure_item", "()"))
            else:
                content.extend(self._generate_property(state, "get_item_state"))

        content.append('@property')
        content.append('def _property__rect_min2(self, /, *, __func=_dearpygui.get_item_state):')
        content.append('    return __func(self)["pos"]')
        content.append('')
        content.append('@property')
        content.append('def _property__rect_max2(self, /, *, __func=_dearpygui.get_item_state):')
        content.append('    state = __func(self)')
        content.append('    x_pos, y_pos  = state["pos"]')
        content.append('    width, height = state["rect_size"]')
        content.append('    return [x_pos + width, y_pos + height]')
        content.append('')

        content.append('')


        content.append("# scroll behaviors")
        content.append("")
        for s in ("x", "y"):
            content.append(f'@property')
            content.append(f'def _property__{s}_scroll_max(self, /, *, __func=_dearpygui.get_{s}_scroll_max):')
            content.append(f'    return __func(self)')
            content.append('')
            content.append(f'@property')
            content.append(f'def _property__{s}_scroll_pos(self, /, *, __func=_dearpygui.get_{s}_scroll):')
            content.append(f'    return __func(self)')
            content.append(f'@_property__{s}_scroll_pos.setter')
            content.append(f'def _property__{s}_scroll_pos(self, value, /, *, __func=dearpygui.set_{s}_scroll) -> None:')
            content.append(f'    __func(self, value)')
            content.append('')

        content.append("")

        content.extend(("# configuration properties", ""))
        for name in sorted(self._shared_properties):
            param = self._shared_properties[name]
            null  = self._get_configuration_null_value(param)

            if null is not None:
                content.extend(self._generate_property(name, "get_item_configuration", "configure_item", null))
            else:
                content.extend(self._generate_property(name, "get_item_configuration", "configure_item"))

        content.extend(('',) * 3)


        content.append("# [ type definitions ]")
        content.append('')
        content.append("# NOTE: Some assignments refer to objects that may not exist")
        content.append("# during class creation. These are set near the end of the module.")
        content.append('')
        content.extend(buffer)
        content.append('')
        content.append("# late assignments")
        content.append('')

        with apply_linebreak(content):
            for type_name, assignments in self._late_assignments.items():
                for var, value in assignments.items():
                    content.append(f"{type_name}.{var} = {value}  # type: ignore")

        local_vars = (
            ("parents", self._is_common_parent, self._is_common_child, FeatureFlag.CHILD),
            ("children", self._is_common_child, self._is_common_parent, FeatureFlag.PARENT),
        )
        for var, pred_global_value, filter_mthd, filter_flag in local_vars:
            global_name  = f"_COMMON_{var.upper()}"
            global_value = ', '.join(ti.name for ti in self.metadata.typedef if pred_global_value(ti))
            content.append(f"{global_name} = ({global_value})")

            for type_info in self.metadata.typedef:
                assignment_name = f"{type_info.name}.__item_{var.lower()}__"
                if filter_mthd(type_info):
                    assignment_value = global_name
                elif type_info.flags & filter_flag:
                    s = ', '.join(symbol for symbol in getattr(type_info, var))
                    assignment_value = f"({s},)" if s else "()"
                else:
                    continue
                content.append(f"{assignment_name} = {assignment_value}  # type: ignore")

            content.append(f"del {global_name}")
            content.append('')

        content.append('')

        apply_newlines(content)

        return content


class ItemsStubGenerator(_ItemsGenerator):

    class _NodeTransformer(ast.NodeTransformer):

        def visit_ClassDef(self, node: ast.ClassDef) -> typing.Any:
            if len(node.body) == 1:
                n = node.body[0]
                node_type = type(n)
                if (
                    node_type is ast.Pass or
                    (
                        node_type is ast.Expr and
                        isinstance(n.value, ast.Constant) and  # type: ignore
                        n.value.value is ...  # type: ignore
                    )
                ):
                    node.body.clear()

                    return node

            return self.generic_visit(node)

        def visit_FunctionDef(self, node: ast.FunctionDef) -> typing.Any:
            if (
                len(node.body) > 0 and isinstance((n := node.body[0]), ast.Expr) and
                isinstance(n.value, ast.Constant) and isinstance(n.value.value, str)
            ):
                node.body = [n, ast.Expr(ast.Constant(...))]
            else:
                node.body = [ast.Expr(ast.Constant(...))]

            return self.generic_visit(node)

    def _generate_dataproperty(self, parameter: metadata.Parameter, /, null_value = None, *, level: int = 0, get_type: str | None = None, set_type: str | None = None):
        name = parameter.name
        anno = parameter.annotation
        if anno is parameter.empty:
            anno = "Any"

        if get_type is None:
            get_type = anno
        if set_type is None:
            set_type = anno

        buffer = []

        buffer.extend((
            f"@property",
            f"def {name}(self, /) -> {get_type}: ...",
        ))
        if not (parameter.flags & ParameterFlag.NO_WRITE):
            buffer.extend((
                f"@{name}.setter",
                f"def {name}(self, value: {set_type}, /) -> None: ...",
            ))

            if null_value is not None:
                buffer.extend((
                    f"@{name}.deleter",
                    f"def {name}(self, /) -> None: ...",
                ))

        apply_indents(buffer, level)

        return buffer

    def _generate_class_entry(self, type_info: ItemTypeInfo, /, *, level: int = 0):
        buffer = []

        type_name = type_info.name


        # class declaration

        V = P = C = ", None"

        value_type = type_info.value_type
        inner_value_type = None
        if value_type is not None:
            V = ', V'
            buffer.append(f"class {type_name}[U = Any, V: {value_type} = Any")
        else:
            buffer.append(f"class {type_name}[U = Any")

        flags = type_info.flags
        bases = []

        if flags & FeatureFlag.ADDRESSABLE:
            # `typing.get_args()` doesn't work on annotations that can't be resolved
            inner_value_type = value_type.removeprefix("Array[").rstrip("]")
            if inner_value_type.endswith("Any"):
                inner_value_type = inner_value_type.removesuffix("Any").rstrip(", ")
            else:
                inner_value_type = inner_value_type.rpartition("Literal")[0].rstrip(", ")
            assert value_type != inner_value_type

            bases.append(f"SupportsValueArray[{inner_value_type}]")
        del value_type, inner_value_type

        if flags & FeatureFlag.CHILD:
            P = ", P"
            buffer.append(", P: ")
            if not type_info.parents:
                buffer.append("ContainerItem[Any, Any, Any, Any] = ContainerItem")
            elif len(type_info.parents) <= 3:
                buffer.append(" | ".join(parent for parent in type_info.parents) + " = Any")
            else:
                buffer.append("ContainerItem[Any, Any, Any, Any] = ContainerItem")
            bases.append("ChildItem[U{V}{P}{C}]")

        if flags & FeatureFlag.PARENT:
            C = ", C"
            bases.append("ContainerItem")
            buffer.append(", C: ")
            if type_name.endswith("HandlerRegistry"):
                buffer.append(f"_HandlerItem[Any, {type_name}] = _HandlerItem")
            elif type_name.endswith("ThemeComponent"):
                buffer.append(f"_ElementItem[Any, Any, {type_name}] = _ElementItem")
            else:
                buffer.append(f"ChildItem[Any, Any, {type_name}, Any] = ChildItem")
            bases.append("ContainerItem[U{V}{P}{C}]")

        buffer.append(']')
        del flags

        assert len(bases) > 0
        buffer.append(f"({', '.join(bases)}):")
        buffer.append(''.join(buffer).format(V=V, P=P, C=C))
        del buffer[:-1], bases, V, P, C


        members_seen = {"ok", "pos"}


        # constructor

        parameters = []

        for p in type_info.parameters:
            anno = p.annotation
            if anno is not p.empty and p.default == 'None' and not anno.endswith('None'):
                p = p.replace(annotation=f"{anno} | None")

            parameters.append(p)
        # HACK: make the `item_type` argument of `mvThemeComponent.create()` compatible
        # with item type classes
        if type_name == "mvThemeComponent":
            try:
                i, p = next((i, p) for i, p in enumerate(parameters) if p.name == "item_type")
            except StopIteration: pass
            else:
                parameters[i] = p.replace(annotation="type[AppItem] | int")

        signature = repr(inspect.Signature(parameters)) \
            .partition('(')[-1] \
            .rpartition(')')[0] \
            .replace("user_data: Any = None", "user_data: U = ...")

        buffer.append(f"    @classmethod")
        buffer.append(f"    def create(cls, {signature}, **kwargs) -> Self: ...  # ty: ignore[invalid-method-override]")
        members_seen.add("create")
        del parameters  # keep `signature` because we re-use it for the functional API


