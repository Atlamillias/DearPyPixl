def apply_indents(buffer: list[str], level: int = 0, /) -> None:
    prefix = "    " * max(level, 0)

    for i, s in enumerate(buffer):
        buffer[i] = f"{prefix}{s}"


def apply_newlines(buffer: list[str], newline: bool = True, /) -> None:
    suffix = '\n' if newline else ''
    for i, s in enumerate(buffer):
        buffer[i] = f"{s.rstrip('\n')}{suffix}"


@contextlib.contextmanager
def apply_linebreak(buffer: list[str], count: int = 1, /):
    line_no = len(buffer)
    try:
        yield
    finally:
        if count > 0 and len(buffer) > line_no:
            buffer.extend(('',) * count)


class FileGenerator(typing.Protocol):
    __slots__ = ("metadata", "module")

    mimetype: typing.ClassVar[str] = "application/text"
    module_name: typing.ClassVar[str] = ''

    metadata: DearPyGuiMetadata
    module: types.ModuleType

    def __init__(self, metadata: DearPyGuiMetadata, /) -> None:
        self.metadata = metadata

        module_name = self.module_name.removesuffix(".py")
        if not module_name:
            self.module = types.ModuleType('')
            return

        module = sys.modules.get(module_name)
        if module is None:
            fpath = pathlib.Path(__file__).parent / f"{module_name}.py"

            module_spec = importlib.util.spec_from_file_location(module_name, fpath)
            if module_spec is not None:
                module = importlib.util.module_from_spec(module_spec)
                loader = module_spec.loader
                assert loader is not None
                loader.exec_module(module)

                sys.modules[module.__name__] = module
            else:
                module = types.ModuleType(module_name)

        self.module = module

    def _generate_banner(self, title: str, /) -> list[str]:
        dpg_version = self.metadata.version
        if not dpg_version:
            dpg_version = "<unknown>"
        return [
            f"############################################################",
            f"# {title}",
            f"#",
            f"# DearPyPixl version: {DPX_VERSION}",
            f"# DearPyGui version: {dpg_version}",
            f"#",
            f"# NOTE: This file is automatically generated - changes may be",
            f"# overwritten.",
            f"############################################################",
        ]

    def generate(self, /) -> list[str]:
        return []


@dataclasses.dataclass(slots=True, frozen=True)
class FileSpec[T1: type[FileGenerator], T2: type[FileGenerator]]:
    name: str
    path: str = ''
    codegen: T1 = FileGenerator  # ty: ignore
    stubgen: T2 = FileGenerator  # ty: ignore

    def __hash__(self, /) -> int:
        return hash((self.path, self.name))

    def __eq__(self, other, /) -> bool:
        try:
            return self.__hash__() == hash(other)
        except TypeError:
            return False

    __registry = {}

    registry: typing.ClassVar[typing.Mapping[str, FileSpec]] = type(int.__dict__)(__registry)

    def register(self, /) -> typing.Self:
        name = self.name.lower().strip()
        path = self.path.lower().strip()
        if path:
            key = f"{path}.{name}"
        else:
            key = name

        registry = __class__.__registry
        if key in registry:
            raise ValueError(f"{key!r} is already registered")

        registry[key] = self

        return self

    def writefiles(self, dirpath: pathlib.Path | str, metadata: DearPyGuiMetadata, /):
        dirpath = Path(dirpath)
        if self.path:
            dirpath /= self.path.strip("./\\").replace(".", "/")

        for filegen, ext in ((self.codegen, "py"), (self.stubgen, "pyi")):
            source = filegen(metadata).generate()
            if source:

                fp = dirpath / f"{self.name}.{ext}"
                dp.mkdir(parents=True, exist_ok=True)
                with open(fp, 'w') as file:
                    file.writelines(source)




