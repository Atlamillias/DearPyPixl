class _ElementFileGenerator(FileGenerator, typing.Protocol):
    imports: typing.Sequence[str]
    prefixes: typing.Sequence[str]

    def _generate_function(self, name: str, category: str, target: str, /) -> typing.Sequence[str]: ...

    mimetype = "text/x-python"

    def _iter_constant_info(self, constants: typing.Iterable[str], /) -> typing.Iterator[tuple[str, ...]]:
        pattern = re.compile(r"[A-Z][a-z0-9]*")

        for constant in constants:

            prefix, _, suffix = constant.partition('_')
            if prefix not in self.prefixes:
                continue

            match prefix:
                case "mvThemeCol":
                    prefix   = ''
                    category = "mvThemeCat_Core"
                case "mvPlotCol":
                    prefix   = "plot"
                    category = "mvThemeCat_Plots"
                case "mvNodeCol" | "mvNodesCol":
                    prefix   = "node"
                    category = "mvThemeCat_Nodes"
                case "mvStyleVar":
                    prefix   = ''
                    category = "mvThemeCat_Core"
                case "mvPlotStyleVar":
                    prefix   = "plot"
                    category = "mvThemeCat_Plots"
                case "mvNodeStyleVar" | "mvNodesStyleVar":
                    prefix   = "node"
                    category = "mvThemeCat_Nodes"
                case _:
                    continue

            words = []

            if prefix:
                words.append(prefix)

            for substring in suffix.split("_"):
                for word in pattern.findall(substring):
                    word = word.lower()
                    if word == prefix:
                        continue
                    words.append(word)

            name = '_'.join(words).replace("background", "bg").replace("foreground", "fg")

            yield name, category, constant

    def generate(self, /):
        content = []

        content.extend(self.imports)
        content.extend(("", ""))

        content.append("__all__ = (")

        buffer = []
        for name, category, target in self._iter_constant_info(self.metadata.constants):
            content.append(f'    "{name}",')
            buffer.extend(self._generate_function(name, category, target))
        content.extend((")","","","",""))

        content.extend(buffer)

        apply_newlines(content)
        return content


